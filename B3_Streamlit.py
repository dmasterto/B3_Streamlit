st.header("**PAINEL DE PRECOS E DIVIDENDOS DE ACOES DA B3**")

ticker = st.text_input("Digite o ticker da acao","BBAS3")
empresa = yf.Ticker(f"{ticker}.SA")

tickerDF = empresa.history(period ="1d",
                           start = "2014-01-01",
                           end = "2025-10-30")

col1, col2, col3 = st.colums([1,1,1])

with col1:
  st.write(f"**Empresa:** {empresa.info['longName']}")
with col2:
  st.write(f"**Setor:** {empresa.info['industry']}")
with col1:
  st.write(f"**Pre√ßo Atual:** {empresa.info['currentPrice']}")

st.line_chart(tickerDF.Close)
st.bar_chart(tickerDF.Dividends)